#include <iostream>

int main() {
    // Define two numbers to be added
    int number1 = 5;
    int number2 = 7;
    
    // Calculate the sum
    int sum = number1 + number2;
    
    // Print the result
    std::cout << "The sum of " << number1 << " and " << number2 << " is: " << sum << std::endl;
    
    return 0;
}
